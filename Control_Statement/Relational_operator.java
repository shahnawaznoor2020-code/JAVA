package Control_Statement;

public class Relational_operator
{
    public static void main(String[] args)
    {
        int a=10;
        int b=20;
        System.out.println(a+" "+b);
        System.out.println((a<b)+" a<b");
        System.out.println((a>b)+" a>b");
        System.out.println((a==b)+" a==b");
        System.out.println((a<=b)+" a<=b");
        System.out.println((a>=b)+" a>=b");
        System.out.println((a!=b)+" a!=b");
        boolean x=true;
        boolean y=false;
        boolean z=(a<b);
        System.out.println(x+" "+y+" "+z);
    }
}
