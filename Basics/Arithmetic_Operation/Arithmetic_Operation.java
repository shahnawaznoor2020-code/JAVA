package Basics;

public class Arithmetic_Operation
{
    public static void main(String[] args)
    {
        double x=5,y=2;
        System.out.println("x: "+x);
        System.out.println("y: "+y);
        System.out.println("x+y: "+(x+y));
        System.out.println("x-y: "+(x-y));
        System.out.println("x*y"+(x*y));
        System.out.println("x/y"+(x/y));
    }
}
