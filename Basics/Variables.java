package Basics;

public class Variables
{
    public static void main(String[] args)
    {
        int x=5;
        System.out.println(x+7);
        int y;
        y=20;
        System.out.println(y);
        x=x+7;
        System.out.println(x);
        x=x-2;
        System.out.println(x);
        x=x*10;
        System.out.println(x);
        x=x/5;
        System.out.println(x);
    }

}
